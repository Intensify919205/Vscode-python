from turtle import*
t=Turtle()
s=Screen()
t.speed(15)
t.width(5)
s.bgcolor("black")
t.color("white")
#making stars
def star(x,y):
  t.pu()
  t.goto(x,y)
  t.pd()
  for i in range(5):
    t.fd(10)
    t.rt(144)

star(-10,100)
